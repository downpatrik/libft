#include "libft.h"

int		ft_strcmp(char *s1, char *s2)
{
	while ((t_u8)*s1 == (t_u8)*s2 && *s1)
	{
		s1++;
		s2++;
	}
	return (*(t_u8*)s1 - *(t_u8*)s2);
}
